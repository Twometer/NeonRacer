package neonracer.stats;

public class CarStats {

    private int place;

    private int lapsPassed;

    public int getPlace() {
        return place;
    }

    public void setPlace(int place) {
        this.place = place;
    }

    public int getLapsPassed() {
        return lapsPassed;
    }

    public void setLapsPassed(int lapsPassed) {
        this.lapsPassed = lapsPassed;
    }
}
